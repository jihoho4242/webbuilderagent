    def headline_for(theme)
      case theme
      when "editorial-luxury" then "A restrained first view with room for the brand to breathe"
      when "trust-service" then "Clear next steps, calm proof, and no unsupported claims"
      when "commerce-mobile" then "A shoppable mobile path with every claim marked draft-safe"
      else "A focused product story that makes the primary action obvious"
      end
    end

    def summary_for(theme)
      base = case theme
             when "editorial-luxury" then "Editorial hierarchy, measured whitespace, and source-safe content slots translate the approved route into an implementation-ready visual direction."
             when "trust-service" then "Trust cues, contact or action clarity, and practical service structure are prioritized without inventing reviews, credentials, or results."
             when "commerce-mobile" then "Product browsing, action placement, and reassurance modules are organized for small screens while keeping all product claims as placeholders."
             else "The candidate emphasizes conversion clarity, reusable components, route-specific constraints, and visual hooks ready for downstream code generation."
             end
      "#{base} Primary interaction: #{primary_interaction}."
    end

    def reference_card_html
      return "" unless reference_brief_present?

      <<~HTML.chomp
        <div class="card" data-aiweb-id="candidate.reference-basis">
          <strong>Reference basis</strong>
          <p>Use `.ai-web/design-reference-brief.md` for pattern grounding only: hierarchy, CTA, layout, visual language, responsive behavior, and trust cues. Do not copy exact reference UI, copy, prices, trademarks, or signed image URLs.</p>
        </div>
      HTML
    end

    def reference_basis_markdown
      return "Reference basis: none recorded; generated from deterministic local design artifacts only." unless reference_brief_present?

      summary = reference_brief_summary
      <<~MD.rstrip
        ## Reference basis
        - Source: `.ai-web/design-reference-brief.md`
        - Use only pattern-level guidance from the reference brief; avoid exact copying, brand imitation, signed image URL dependence, prices, trademarks, or reference-specific copy.
        #{summary.empty? ? "- Summary: reference brief present; review it before selecting a candidate." : "- Summary: #{summary}"}
      MD
    end

    def reference_brief_present?
      reference_brief.length >= 40
    end

    def reference_brief
      @reference_brief ||= begin
        path = File.join(@aiweb_dir, "design-reference-brief.md")
        File.file?(path) ? File.read(path).to_s.strip : ""
      end
    end

    def reference_brief_summary
      reference_brief
        .lines
        .map(&:strip)
        .reject { |line| line.empty? || line.start_with?("#") || line.start_with?("!") }
        .first(2)
        .join(" ")
        .gsub("|", "/")
        .slice(0, 220)
        .to_s
    end

    def first_view_obligations
      Array(intent["must_have_first_view"]).reject { |item| item.to_s.strip.empty? }
    end

    def primary_interaction
      value = intent["primary_interaction"].to_s.strip
      value.empty? ? "approved first-view action" : value
    end

    def market_route
      present(intent["market_archetype"], intent["archetype"] || "fallback")
    end

    def design_system_id
      from_design = design_markdown[/Selected design system ID:\s*([^\n]+)/, 1]
      present(from_design, intent["recommended_design_system"] || "luxury-editorial")
    end

    def skill_id
      from_design = design_markdown[/Selected skill ID:\s*([^\n]+)/, 1]
      present(from_design, intent["recommended_skill"] || "premium-landing-page")
    end

    def present(value, fallback)
      string = value.to_s.strip
      string.empty? ? fallback : string
    end

    def theme_color(theme)
      case theme
      when "trust-service"
        { paper: "#f7f3ec", panel: "#efe5d6", accent: "#6f5b32", line: "#ded2c1" }
      when "conversion-product"
        { paper: "#f6f8fb", panel: "#e7edf7", accent: "#3057d5", line: "#d8e0ee" }
      when "commerce-mobile"
        { paper: "#fbf7f2", panel: "#f3e6da", accent: "#a85e2a", line: "#e5d7ca" }
      else
        { paper: "#f5f1ea", panel: "#e8dfd1", accent: "#8b6c38", line: "#d8cdbc" }
      end
    end

    def hero_columns(theme)
      case theme
      when "editorial-luxury" then "1.25fr .75fr"
      when "commerce-mobile" then ".9fr 1.1fr"
      else "1fr 1fr"
      end
    end

    def card_radius(theme)
      theme == "conversion-product" ? "18px" : "2px"
    end

    def escape_html(value)
      value.to_s.gsub("&", "&amp;").gsub("<", "&lt;").gsub(">", "&gt;")
    end

    def escape_attr(value)
      escape_html(value).gsub('"', "&quot;")
    end
  end
end
