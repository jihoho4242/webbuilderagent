            .eyebrow { color: var(--accent); text-transform: uppercase; letter-spacing: .14em; font-size: 12px; font-weight: 760; }
            h1 { margin: 0; font-size: clamp(42px, 7vw, 92px); line-height: .91; letter-spacing: -.075em; max-width: 850px; }
