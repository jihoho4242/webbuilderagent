                  <strong>First-view obligations</strong>
                  <ul>
                    #{obligations}
                  </ul>
                </section>
                <section class="card" data-aiweb-id="candidate.#{id}.layout-notes">
                  <strong>Layout direction</strong>
                  <p>#{escape_html(spec.fetch(:layout))}</p>
                </section>
                <section class="card" data-aiweb-id="candidate.#{id}.tradeoff-notes">
                  <strong>Review tradeoffs</strong>
                  <p>#{escape_html(spec.fetch(:tradeoffs).join('; '))}</p>
                </section>
              </div>
            </article>
            <div class="mobile-bar" data-aiweb-id="candidate.#{id}.mobile-sticky-action">Mobile sticky action placeholder — source-safe</div>
          </main>
          <!-- aiweb:visual-contract:end #{id} -->
        </body>
        </html>
